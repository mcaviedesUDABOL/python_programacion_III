# operadores de asignacion (=, +=, -=, *=, /=, **=)
#operador =
numero = 9
print(numero)

#operador +=
numero = numero + 4
print(numero)
# esto es igual a decir
numero = 9
numero += 4
print(numero)

#operador -=
numero = 9
numero = numero - 4
print(numero)
# esto es igual a decir
numero = 9
numero -= 4
print(numero)

#operador -=
numero = 9
numero = numero * 4
print(numero)
# esto es igual a decir
numero = 9
numero *= 4
print(numero)

#operador /=
numero = 9
numero = numero / 4
print(numero)
# esto es igual a decir
numero = 9
numero /= 4
print(numero)

#operador **=
numero =2
numero = numero ** 3
print(numero)
# esto es igual a decir
numero =2
numero **=3
print(numero)