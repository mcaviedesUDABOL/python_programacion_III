# operadores logicos (and, or, not)

numero1 = 10
numero2 = 5
numero3 = 7
numero4 = 8
# and
print(numero1 > numero2)

print(numero3 < numero4)

print((numero1 > numero2) and (numero3 > numero4) ) 

# or

print((numero1 > numero2) or (numero3 > numero4) ) 

# not
print(numero3 < numero4)
print( not(numero3 > numero4))
