#funciones de cadena
cadena = "Hola Mundo"

# longitud de cadena
print( len(cadena) )

# mostrar cadena en a mayusculas
print( cadena.upper() )

#convertir cadena a mayuscular
print(cadena)
cadena = cadena.upper()
print(cadena)

# mostrar lista de metodos de cadena ctrl + espace

#mostrar cadena en minuscula
print(cadena.lower())

#dividir la cadena en las palabras que la componen espacio en blanco
print(cadena.split())

# dividir cadena por un caracter en especial ','
cadena2 = "uva,pera,manzana,maranja"
print(cadena2)
print(cadena2.split(','))



